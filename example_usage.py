import cv2
import numpy as np
from face_recognizer import YOLOFaceRecognizer
import os
from face_recognizer import FaceRecognizer

def main():
    # 初始化人脸识别器
    recognizer = YOLOFaceRecognizer(
        yolo_model='yolov8n.pt',  # 可以替换为其他YOLO模型，如'yolov8s.pt', 'yolov8m.pt'等
        face_detector_threshold=0.5,
        face_recognizer_threshold=0.6
    )
    print("人脸识别器初始化完成")

    # ------------------------------
    # 示例1: 处理单张图像 - 人脸检测
    # ------------------------------
    try:
        # 读取图像
        image = cv2.imread('test_image.jpg')
        if image is None:
            raise FileNotFoundError("测试图像未找到，请确保当前目录下有test_image.jpg文件")

        # 处理图像（仅检测，不识别）
        processed_image, results = recognizer.process_image(image)

        # 显示结果
        cv2.imshow('Face Detection Demo', processed_image)
        cv2.waitKey(0)
        cv2.destroyAllWindows()

        # 打印检测结果
        print("\n图像人脸检测结果:")
        for i, result in enumerate(results):
            print(f"人脸 {i+1}: 位置={result['bbox']}, 状态=未知")
    except Exception as e:
        print(f"处理图像时出错: {str(e)}")

    # ------------------------------
    # 示例2: 添加已知人脸并进行识别
    # ------------------------------
    try:
        # 假设我们有已知人脸的图像
        # 这里使用同一图像中的人脸作为示例，实际应用中应使用不同的人脸图像
        known_face_image = cv2.imread('known_face.jpg')
        if known_face_image is None:
            raise FileNotFoundError("已知人脸图像未找到，请确保当前目录下有known_face.jpg文件")

        # 检测并提取已知人脸特征
        known_faces = recognizer.detect_faces(known_face_image)
        if known_faces:
            known_face_feature = recognizer.extract_face_feature(known_faces[0][0])
            if known_face_feature is not None:
                # 添加到已知人脸库
                recognizer.add_known_face("目标人物", known_face_feature)
                print("\n已添加已知人脸到识别库")

        # 再次处理测试图像，现在会尝试识别人脸
        image = cv2.imread('test_image.jpg')
        if image is not None:
            processed_image, results = recognizer.process_image(image)
            cv2.imshow('Face Recognition Demo', processed_image)
            cv2.waitKey(0)
            cv2.destroyAllWindows()

            # 打印识别结果
            print("\n图像人脸识别结果:")
            for i, result in enumerate(results):
                print(f"人脸 {i+1}: 位置={result['bbox']}, 名称={result['name']}, 相似度={result['similarity']:.2f}")
    except Exception as e:
        print(f"添加已知人脸时出错: {str(e)}")

    # ------------------------------
    # 示例3: 处理视频文件
    # ------------------------------
    # 处理图像示例 (注释掉缺失的图像文件处理)
    # try:
    #     image_results = face_recognizer.process_image('test_image.jpg')
    #     print("图像识别结果:", image_results)
    # except Exception as e:
    #     print(f"处理图像时出错: {e}")
    
    # 添加已知人脸示例 (注释掉缺失的图像文件处理)
    # try:
    #     face_recognizer.add_known_face('known_face.jpg', 'Known Person')
    #     print("已知人脸添加成功")
    # except Exception as e:
    #     print(f"添加已知人脸时出错: {e}")
    
    # 处理视频示例 (修改为使用摄像头)
    try:
        print("开始从摄像头捕获...")
        # 尝试多个摄像头索引
        for camera_index in [0, 1, -1]:
            try:
                recognizer.process_video(camera_index)
                break
            except ValueError as e:
                print(f"尝试摄像头索引 {camera_index} 失败: {e}")
        else:
            print("所有摄像头索引都无法打开，请检查摄像头连接")
            print("备选方案: 使用静态图像进行人脸识别测试")
            test_image_path = input("请输入测试图像路径(留空使用默认示例): ") or "test_image.jpg"
            if not os.path.exists(test_image_path):
                print(f"错误: 文件 '{test_image_path}' 不存在，请检查路径是否正确。")
            else:
                try:
                    image_results = recognizer.process_image(test_image_path)
                    print("图像识别结果:", image_results)
                except Exception as e:
                    print(f"处理图像时出错: {e}")
    except Exception as e:
        print(f"处理视频时出错: {e}")

if __name__ == '__main__':
    main()
